package day21;

public class WarmUp {
	public static void main(String[] args) {
		// initial part condition update part
		for (int i = 0; i < 10; i++) {
			System.out.println(i + ". Tuesday class");
		}
		System.out.println("-----");
		
		int count = 0;
		for (; count < 5; count+=2) {
			System.out.println("hello");
		}
		System.out.println("-----");
		
		for (char ch = 'a'; ch <= 'z'; ch++) {
			System.out.println(ch + " ");
		}
		System.out.println();
	}

}
