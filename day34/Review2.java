public class Review2 {
	public static void main(String[] args) {
		// What are the wrapper classes?
		// It's object version of primitives
		
		int num = 17;		// primitive
		Integer numW = 17;	// wrapper class
		
		char ch = 'a';
		Character chW = 'a';
		
		// byte    -> Byte
		// short   -> Short
		// int     -> Integer
		// long    -> Long
		// float   -> Float
		// double  -> Double
		// char    -> Character
		// boolean -> Boolean
	}

}
